Execute:

    ; it only works with this block on start:
    ld 		a, 15      	        ; Foreground color
    ld 		(BIOS_FORCLR), a    
    ld 		a, 1  		        ; Background color
    ld 		(BIOS_BAKCLR), a     
    ld      a, 1
    ld 		(BIOS_BDRCLR), a    
    call 	BIOS_CHGCLR             ; Change Screen Color

    call    BIOS_INIGRP                 ; screen 2

    ; load PATTBL
    ld		hl, IBM_PC_FONT         ; RAM address (source)
    ld		de, PATTBL		; VRAM address (destiny)
    ld		bc, IBM_PC_FONT.size	; Block length
    call 	BIOS_LDIRVM        	; Block transfer to VRAM from memory

    ; fill COLTBL
    ld      hl, COLTBL
    call    BIOS_SETWRT
    ld      a, 0xf0         ; foreground white, background black
    ld      bc, 256 * 8
.loop_1:
    out     (PORT_0), a
    dec     bc
    ld      a, b
    or      c
    djnz    .loop_1

    ; fill NAMTBL
    ld      hl, NAMTBL
    call    BIOS_SETWRT
    ld      a, 0
    ld      b, 0        ; 256 iterations
.loop_2:
    out     (PORT_0), a
    inc     a
    djnz    .loop_2

    db      "End ROM started at 0x4000"

    ds PageSize - ($ - 0x4000), 255	; Fill the unused area with 0xff
